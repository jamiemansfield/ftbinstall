package util

import (
	"os"
	"os/exec"
)

// Runs the given program, with given arguments, outputting to the
// console of this program.
func RunCommand(name string, arg ...string) error {
	cmd := exec.Command(name, arg...)
	cmd.Stdout = os.Stdout
	cmd.Stderr = os.Stderr
	return cmd.Run()
}
